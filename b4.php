By James S.A. Corey <br>
Credit: Hachette Book Group <br>
200 years in the future, humanity has colonized the solar system and is split among three factions on the brink of conflict: Earth, Mars and the Asteroid Belt, which includes the spinning Ceres asteroid colony. As multiple viewpoint characters are ensnared in a system-wide mystery, the story's scope slowly broadens to reveal the full complexity of the novels' science fiction world. The books, co-written by Dan Abraham and Ty Franck, originally stemmed from a tabletop roleplaying game idea, and it shows through the detailed worldbuilding and exploration of a solar system remade in humanity's image. Plus, it's a fun, tightly-plotted set of spacefaring adventure stories. <hr>

3.'The Martian' (Random House, 2014)<br>
By Andy Weir <br>
Credit: Crown <br>
"The Martian," by Andy Weir, is a truly great science fiction book that's heavy on the science. Weir tells the story of Mark Watney, a fictional NASA astronaut stranded on Mars, and his difficult mission to save himself from potential doom in the harsh Red Planet environment. Watney seems to have everything against him, yet Weir deftly explains not only what Watney's survival needs are but also how he goes about trying to make them work. "The Martian" also will be made into a movie, which is set for release in November 2015. The film stars Matt Damon as Watney and is directed by space movie veteran Ridley Scott. ~Miriam Kramer
The series is slated for nine books, and they've appeared steadily one per year from 2011-2015 for a total of five so far (plus some tie-in novellas). They're also the basis for Syfy's TV show "The Expanse," recently renewed for a 13-episode second season. Book six, "Babylon's Ashes," is slated for release December 2016. <hr>

