A young woman plots to find stolen artifacts in "Provenance," which takes place in the same universe as author Ann Leckie's award-winning "Ancillary" trilogy of books but introduces readers to a new selection of future human cultures with a more straightforward and less high-concept adventure story. Don't let that fool you, though: The book's exploration of multiculture, multispecies conflict (with aliens called the Geck) works just as much intriguing worldbuilding into the mix as her previous books. Plus, there are mind-controlled robots, stolen alien ships and a society with three genders.<br> ~Sarah Lewin<hr>

